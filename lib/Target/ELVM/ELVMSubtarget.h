#ifndef LLVM_LIB_TARGET_ELVM_ELVMSUBTARGET_H
#define LLVM_LIB_TARGET_ELVM_ELVMSUBTARGET_H

#include "ELVMFrameLowering.h"
#include "ELVMISelLowering.h"
#include "ELVMInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"

#define GET_SUBTARGETINFO_HEADER
#include "ELVMGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class ELVMSubtarget : public ELVMGenSubtargetInfo {
  virtual void anchor();
  ELVMInstrInfo InstrInfo;
  ELVMFrameLowering FrameLowering;
  ELVMTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  // This constructor initializes the data members to match that
  // of the specified triple.
  ELVMSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
               const TargetMachine &TM);

  // ParseSubtargetFeatures - Parses features string setting specified
  // subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  const ELVMInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const ELVMFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const ELVMTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
  const ELVMRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
};
} // End llvm namespace

#endif
